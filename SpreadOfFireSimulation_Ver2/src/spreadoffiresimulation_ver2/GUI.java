    /**
     * start button will work automatically when isAuto is true
     * otherwise, it will work once only if it is pressed
     * 
     * start button invokes the probCatch method from Fire class
     * if isAuto is true
     * otherwise, it will invoke the stepBurn method from Fire class
     * @param evt 
     */
    private void startbuttActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_startbuttActionPerformed
        final double probFire = Double.parseDouble(fireprobText.getText());
        if (isAuto) {
            Thread t = new Thread(new Runnable() {
                @Override
                public void run() {
                    if (fire.isStop() == true) {
                        //do noting
                    } else {
                        fire.probCatch(probFire);
                        countLabel.setText("STEP COUNT: " + fire.getCount());
                        numTreeLabel.setText("NO. OF TREE: " +forest.getTree());
                    }
                }
            });
            t.start();
        } else {
            Thread t = new Thread(new Runnable() {
                @Override
                public void run() {
                    if (fire.isStop() == true) {
                        //do nothing
                    } else {
                        fire.stepBurn(probFire);
                        countLabel.setText("STEP COUNT: " + fire.getCount());
                        numTreeLabel.setText("NO. OF TREE: " +forest.getTree());
                    }
                }
            });
            t.start();
        }
    }//GEN-LAST:event_startbuttActionPerformed

    /**
     * the combo boxes will be selected to set size of the forest
     * @param evt 
     */
    private void setSizeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_setSizeActionPerformed
        if(setSize.getModel().getSelectedItem().equals("large")){
            size = 100;
        } else if(setSize.getModel().getSelectedItem().equals("big")){
            size = 50;
        } else if(setSize.getModel().getSelectedItem().equals("medium")){
            size = 20;
        } else {
            size = 10;
        }
        jLabel4.setText("Forest Size: " + size + " %");
        Thread t = new Thread(new Runnable() {
            @Override
            public void run() {
                forest = new Forest(size);
                simu.newSimu(forest.getSize());
            }
        });
        t.start();
    }//GEN-LAST:event_setSizeActionPerformed

