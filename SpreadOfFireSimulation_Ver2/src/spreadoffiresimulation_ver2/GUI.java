    /**
     * set all inputs in their places
     * the error message will be invoked, if there is any empty input
     * @param evt 
     */
    private void setbuttActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_setbuttActionPerformed
        try {
            double den = Double.parseDouble(densityText.getText());
            double probBurn = Double.parseDouble(probBurnText.getText());
            forest = new Forest(size, den, probBurn);
            simu.newSimu(size);
            numTreeLabel.setText("NO. OF TREE: " +forest.getTree());
            startbutt.setEnabled(true);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Please insert every inputs",
                "Error 404 : Boxes need input", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_setbuttActionPerformed

